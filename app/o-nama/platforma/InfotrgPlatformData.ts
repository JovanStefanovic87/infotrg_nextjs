const h4Blocks = listOfLinks.filter((item) => item.type === 'h4');

const replaceSpecialCharacters = (text: string): string => {
  const specialCharsMap: { [key: string]: string } = {
    č: 'c',
    ć: 'c',
    ž: 'z',
    đ: 'dj',
    š: 's',
    ' ': '-',
  };

  return text
    .toLowerCase()
    .split('')
    .map((char) => specialCharsMap[char] || char)
    .join('')
    .replace(/[^a-zA-Z0-9-]/g, '')
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '');
};

